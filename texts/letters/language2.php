      <p>ἦμεν μὲν ἡ μήτηρ ἐμοῦ καὶ ἔγωγε σύνεδροι ἐν τῷ οἴκῳ καὶ λεγομένων ἡμῶν ἦρξα λέγειν ἐγὼ περὶ τῆς μουσικῆς Σουηδίας καὶ ἀκουσάσης αὐτῆς ἡ δ’ ἀπεκρίθή μοι λέγων ὅτι Οὐκ οἶδας ὅτι τῆς Σουηδίας ἐστὶ ἡ οἰκία ἡμῶν; θαυμασαμένου ἐμοῦ μὲν εἶδον εἰς αὐτὴν μὴ πιστεύσασα τά ὑπὸ αὐτῆς εἰλεγμένα ἐλάλησα δ’ οὖν ὄτι Οὐκ ᾖδη ἀληθῶς· ἦρξε οὕτως ἡ πορεία μου, ὅτι ἠθέλησα μετὰ τὸν καιρὸν τούτον μαθεῖν τὴν τῆς Σουηδίας γλώττην. ἀλλ’ ὡς μὲν γέγραφα οὔπω γεραιὸς ἦν ἀλλὰ νεὸς πάνυ καὶ οὔτως δ’οὐκ οἶος τ’ ἦ πολλὰ τῷ μαθεῖν βιβλία ἀγοράσαι πολλὰ ἀργύρια μὴ σχήσαντα. ἀλλ’ ἔτι οἷος τ’ ἦν ταύτην τὴν γλῶτταν μαθεῖν καὶ νῦν πόλλους μὲν λόγους ἐν ταύτῃ οἶδα καὶ δὴ καὶ τῇ Συηδίᾳ δ’ ἐλήλυθα ἵνα ἐν ἐκείνῃ τὴν γλῶτταν λαλήσω. </p>

      <p>ἀλλὰ μῶν ἐκείνη ἐστὶ ἡ μόνη γλῶττα ἣν μεμάθηκα; οὐκ ἐστὶ, μεμάθηκα μὲν γὰρ πολλάς γλώττας αἳ δ’οὐ μ’ ἐπαιδεύθησαν ὑπὸ διδασκάλων τινῶν ἀλλ’ αἳ μὴ σχήσας ἀγρύρια πολλὰ μ’ ἐπαιδεύθησαν ὑπ’ ἐμαυτοῦ. ἐλθὼν μὲν τῇ τῆς πόλεώς μου βιβλιοθήκῃ καὶ εὑρὼν βιβλίον τι περὶ τῆς μανθανομένης γλώττης δ’ ἦρξα ἐκείνην μαθεῖν μόνον τῷ βιβλίῳ ὃ ἐδανισάμην. μεμάθηκα μὲν ὅσας γλώττας ὅτι νῦν δ’ οὖν πάντα τὰ τῆς κατ’ ἐμαυτὸν μαθούσης γλώττης ὀνόματα γράψαι ἀδύνατόν μοι εἴη.</p>

      <p>τίνα μὲν οὖν ἄλλους λόγους περὶ τῶν γλωττῶν γράψαιμι οὐκ οἶδα καὶ δ’ οὐκοὺν νῦν ταύτην τὴν ἐπιστολὴν τελῶ.</p>

