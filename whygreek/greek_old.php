  <p>This is a question that gets asked rather frequently, not only with my learning Ancient Greek but also Ancient Egyptian. I find it somewhat difficult to give one exact answer, so I shall herein attempt to somewhat explain why I like studying ancient languages such as these, with the focus being on Ancient Greek in this particular text.</p>

  <p>Firstly, I am not generally someone to talk to tonnes of people so that studying <i>regular</i> languages is frequently somewhat difficult for me. Finding a suitable language partner that is willing to speak to your in their native language and endure your countless errors and incomprehensible sentences is not an easy task to accomplish either. Indeed, the latter was particularly difficult — if not to say impossible — for me when it comes to Swedish; I had messaged probably close to one hundred people whose native language was Swedish without any luck. I would either be completely ignored, or the person I was talking to would abruptly stop speaking to me for no discernable reason. This, in turn, has the rather annoying side-effect of my Swedish conversational skills <i>still</i> being rather poor, even after having studied the language for over six years; and even though my Swedish was complimented whilst I was in Sweden, a large amount of people still seemed to wish to speak English to me — something I simply ignored by continuing to speak in Swedish.</p>

  <p>All the above-mentioned reasons often lead me to study languages solely for the purpose of reading books — or other print-media — or listening to and watching things. Therefore, I had always wished to study an ancient language; for if I am going to be studying a language merely for being able to read things that have been written in it, I might as well read texts written thousands of years ago in their original language. And as one of my teachers so rightly said, reading these words from days long past is like being transported to that time; it is the closest thing that we have to time-travel. Of course it would be possible to simply read a translated version of said texts, but translation can never be as good as the original and if you read texts in their original language, it is as if you are actually listening to the writer speak to you directly from the past. This is true with all languages — both new and old —, but the time span between the present and the text’s publication is obviously far greater with texts from Ancient Greek than it is with texts written in the 19th century, for example.</p>
