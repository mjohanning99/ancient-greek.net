      <p>The second commandment of the teaching (is the following): you shall not kill, you shall not commit adultery, you shall not molest children, you shall not steal, you shall not be a user of magic arts, you shall not use drugs, you shall not kill a child in abortion nor kill that which has been born; you shall not desire (to have) that which your neighbour has, you shall not swear, you shall not bear false witness, you shall not speak ill (of people), you shall not hold grudges. You should not fluctuate between two different opinions (lit. two-minded) and you should not be a hypocrite (lit. two-tongued). Hypocritism is death’s trap. Your words shall not be false (you shall not lie), nor shall they be empty (vain, fruitless); instead, they shall be filled with deeds. You shall neither be greedy, nor a robber, nor a hypocrite, nor malicious, nor arrogant. You shall not plan on doing evil to your neighbour. You shall not hate all humanity (all men, every person), but some you shall refute, some you shall pray for and some you shall love more than your own soul (more than yourself).</p>
