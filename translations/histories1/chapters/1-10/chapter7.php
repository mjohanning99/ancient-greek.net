      <p>HĒ de hēgemoniē houtō periēlthe, eousa Hērakleideōn es to genos to Kroisou, kaleomenous de Mermnadas.</p>

      <p>ēn Kandaulēs, ton hoi Hellēnes Myrsilon onomazousi, tyrannos Sardiōn, apogonos de Alkaiou tou Hērakleos. Agrōn men gar ho Ninou tou Bēlou tou Alkaiou prōtos Hērakleideōn basileus egeneto Sardiōn, Kandaulēs de ho Myrsou hystatos.</p>

      <p>hoi de proteron Agrōnos basileusantes tautēs tēs chōrēs ēsan apogonoi Lydou tou Atyos, apʼ hoteu ho dēmos Lydios eklēthē ho pas houtos, proteron Mēiōn kaleomenos.</p>

      <p>para toutōn Hērakleidai epitraphthentes eschon tēn archēn ek theopropiou, ek doulēs te tēs Iardanou gegonotes kai Hērakleos, arxantes men epi dyo te kai eikosi geneas andrōn etea pente te kai pentakosia, pais para patros ekdekomenos tēn archēn, mechri Kandauleō tou Myrsou.</p>
