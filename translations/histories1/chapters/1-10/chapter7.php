  <div class="column">
    <div class="article">
      <p>In this manner, the rule, being of the Heraclids (before), came around and ended at the lineage of Croesus; these were were called the Mermnadae.</p>

     <p>Candaules, whom the Greeks called Myrsilus, was a sovereign of Sardis and a descendant of Alcaeus (son) of Heracles. For Agron, (son) of Belus, (son) of Ninus, (son) of Alcaeus was the first of the Heraclids to be king of Sardis and Candaules (son) of Myrsus was the last.</p>

     <p>Those who ruled this land before Agron were descendants of Lydus, (son) of Atys, from whom the entire district has received its name — earlier it was called Maionian.</p>

     <p>The Heraclids, having been entrusted with the rule, held it because of an oracle and because of two female slaves — one of Iardanus and one of Heracles. They ruled for five hundred and five years — which is over twenty-two generations of men — with children succeeding their fathers up until Candaules (son) of Myrsus.</p>

