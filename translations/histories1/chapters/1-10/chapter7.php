      <p>Ἡ δὲ ἡγεμονίη οὕτω περιῆλθε, ἐοῦσα Ἡρακλειδέων ἐς τὸ γένος τὸ Κροίσου, καλεομένους δὲ Μερμνάδας.</p>

      <p>ἦν Κανδαύλης, τὸν οἱ Ἕλληνές Μυρσίλον ὀνομάζουσι, τύραννος Σαρδίων, ἀπόγονος δὲ Ἀλκαίου τοῦ Ἡρακλέος. Ἄγρων μὲν γὰρ ὁ Νίνου τοῦ Βήλου τοῦ Ἀλκαίου πρῶτος Ἡρακλειδέων βασιλεὺς ἐγένετο Σαρδίων, Κανδαύλης δὲ ὁ Μύρσου ὕστατος.</p>

      <p>οἱ δὲ πρότερον Ἄγρωνος βασιλεύσαντες ταύτης τῆς χώρης ἦσαν ἀπόγονοὶ Λυδοῦ τοῦ Ἄτυος, ἀπʼ ὅτευ ὁ δῆμος Λύδιος ἐκλήθη ὁ πᾶς οὗτος, πρότερον Μηίων καλεόμενος.</p>

      <p>παρὰ τούτων Ἡρακλεῖδαι ἐπιτραφθέντες ἔσχον τὴν ἀρχήν ἐκ θεοπροπίου, ἐκ δούλης τε τῆς Ἰαρδάνου γεγονότες καὶ Ἡρακλέος, ἄρξαντες μὲν ἐπὶ δύο τε καὶ εἴκοσι γενεᾶς ἀνδρῶν ἔτεα πέντε τε καὶ πεντακόσια, παῖς παρὰ πατρὸς ἐκδεκόμενος τὴν ἀρχήν, μέχρι Κανδαύλεω τοῦ Μύρσου.</p>
