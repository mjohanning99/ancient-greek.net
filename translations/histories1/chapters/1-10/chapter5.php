      <p>This is the Persian account; and they believe that, through the capture of Troy (Ilium), their hatred towards the Greeks began. About the matter with Io, the Persian account does not agree with the Phoenician. They say that they did not bring her into Egypt by kidnapping her, but rather that she had sex with the ship-master in Argos. After she found out that she was pregnant and being ashamed her parents would find out, she sailed off with the Phoenicians, so that they might not find out. This, at any rate, is what the Persians and Phoenicians say. I will not say that things happened this or that way, but I shall attempt to point out to you the one who began the wrongdoings to the Greeks in the following pages of my story and write about both the great and small cities of men. Some of these, in the days of old, used to be large but a great many of them have turned small; and those that were great in my time once were small. Knowing that human happiness (prosperity) never stays in the same place, I shall write about both.</p>
