    <div class="article">
      <p>Herodotus of (the town of) Halicarnassus here presents his research (inquiries, examinations) so that, firstly, men’s feats are prevented from becoming forgotten (and fading away); and, secondly, to avoid that the great and wondrous deeds, of which some were exhibited by the Greeks and others by foreigners (barbarians), will lose their due fame. The latter includes, especially, the reasons for their (Greeks and foreigners) waging war against one another.</p>

      <p>The learned of the Persians blame the Phoenicians for this. These arrived at the Mediterranean from the Indian Ocean and settled in the country they inhabit to this very day. When they had settled, they immediately began going out on great sea journeys, bringing Egyptian and Phoenician (Assyrian) merchandise to various countries, but especially Argos. This city was, at the time, the grandest of all the cities in the region of Greece.</p>

      <p>On the fifth or sixth day following their arrival, and after the majority of their wares had been sold, there came to the beach a number of women, amongst which was the daughter of the king. Her name, by which she was known even to the Greeks, was Io, daughter of Inachus. As these women were standing at the front of the ship and buying of the merchandise that which they fancied the most, the Phoenicians were encouraging eachother to lay their hands on them. Whereas the majority of the women managed to escape, Io was amongst those captured; and so, throwing the women aboard, they set sail for Egypt.</p>
