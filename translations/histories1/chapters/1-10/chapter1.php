      <p>Hērodotou Halikarnēsseos historiēs apodexis hēde, hōs mēte ta genomena ex anthrōpōn tō chronō exitēla genētai, mēte erga megala te kai thōmasta, ta men Hellēsi ta de barbaroisi apodechthenta, aklea genētai, ta te alla kai di᾽ hēn aitiēn epolemēsan allēloisi.</p>

      <p>Perseōn men nyn hoi logioi Phoinikas aitious phasi genesthai tēs diaphorēs. toutous gar apo tēs Erythrēs kaleomenēs thalassēs apikomenous epi tēnde tēn thalassan, kai oikēsantas touton ton chōron ton kai nyn oikeousi, autika nautiliēsi makrēsi epithesthai, apagineontas de phortia Aigyptia te kai Assyria tē te allē esapikneesthai kai dē kai es Argos. to de Argos touton ton chronon proeiche hapasi tōn en tē nyn Helladi kaleomenē chōrē. apikomenous de tous Phoinikas es dē to Argos touto diatithesthai ton phorton. pemptē de ē hektē hēmerē ap᾽ hēs apikonto, exempolēmenōn sphi schedon pantōn, elthein epi tēn thalassan gynaikas allas te pollas kai dē kai tou basileos thygatera: to de hoi ounoma einai, kata tōuto to kai Hellēnes legousi, Ioun tēn Inachou: tautas stasas kata prymnēn tēs neos ōneesthai tōn phortiōn tōn sphi ēn thymos malista: kai tous Phoinikas diakeleusamenous hormēsai ep᾽ autas. tas men dē pleunas tōn gynaikōn apophygein, tēn de Ioun syn allēsi harpasthēnai. esbalomenous de es tēn nea oichesthai apopleontas ep᾽ Aigyptou.</p>
