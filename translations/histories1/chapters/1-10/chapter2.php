  <div class="column">
    <div class="article"><p>In this manner, the Persians — unlike the Greeks — say (claim?), Io came to Egypt. This was the first of wrongdoings (misdeeds). After this, some of the Greeks (whose names are unknown) came to Tyre (then a Phoenician port)— according to the the Persians — to kidnap the king’s daughter Europa. These Greeks would have been Cretans. So far, things would have been equal between them; but after this, the Persians say that the Greeks did the second wrongdoing (misdeed). For they sailed into Aia and Colchis and on the Phasis river and after having finished the matters for which they came there, they kidnapped the king’s daughter Medea. The king of Colchis sent a herald (messenger) to demand punishment for the kindnapping of his daughter and to demand that she be returned. They, however, answered that they did not receive any penalties for the kidnapping of Io of Argos and would, thus, not be paying any (for the kidnapping of Medea) either.  </p></div>
