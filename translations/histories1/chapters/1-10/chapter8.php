  <div class="column">
    <div class="article">
      <p>Candaules loved his own wife; he fell in love with her so much, in fact, that he considered her to be the most beautiful of all. As he believed this, he greatly praised her beauty even to Gyges (son) of Dascylus, who was the bodyguard he was most satisfied with; so much so that he entrusted him even with the most serious and important matters.</p>

      <p>After not too much time had passed — and something bad was bound to happen to him (at some point) — he said to Gyges the following, <q>Gyges, I do not believe you trust what I say about the beauty of my wife — for, indeed, that which people hear appear to them less trustworthy than what they see —, so let us have it arranged that you shall see her naked.</q></p>

      <p>Protesting loudly at this, he said, <q>Master, why do you say such unclean things by ordering me to see the mistress naked? At the same time that a woman removes her robes, she also removes her modesty. Long ago, certain things were found out by noble men from which one must learn; one of these is to mind your own business. I, for one, believe your saying that she is the most beautiful of women and I implore you not to make me do lawless things.</q></p>
