      <p>Houtos dē ōn ho Kandaulēs ērasthē tēs heōutou gynaikos, erastheis de enomize hoi einai gynaika pollon paseōn kallistēn. hōste de tauta nomizōn, ēn gar hoi tōn aichmophorōn Gygēs ho Daskylou areskomenos malista, toutō tō Gygē kai ta spoudaiestera tōn prēgmatōn hyperetitheto ho Kandaulēs kai dē kai to eidos tēs gynaikos hyperepaineōn.</p>

      <p>chronou de ou pollou dielthontos — chrēn gar Kandaulē genesthai kakōs — elege pros ton Gygēn toiade. <q>Gygē, ou gar se dokeō peithesthai moi legonti peri tou eideos tēs gynaikos — ōta gar tynchanei anthrōpoisi eonta apistotera ophthalmōn —, poiee hokōs ekeinēn theēseai gymnēn.</p>

      <p>ho dʼ ambōsas eipe <q>despota, tina legeis logon ouk hygiea, keleuōn me despoinan tēn emēn theēsasthai gymnēn? hama de kithōni ekdyomenō synekdyetai kai tēn aidō gynē. palai de ta kala anthrōpoisi exeurētai, ek tōn manthanein dei; en toisi hen tode esti, skopeein tina ta heōutou. egō de peithomai ekeinēn einai paseōn gynaikōn kallistēn, kai seo deomai mē deesthai anomōn.</q></p>
