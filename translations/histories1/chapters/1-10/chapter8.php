      <p>Οὗτως δὴ ὦν ὁ Κανδαύλης ἠράσθη τῆς ἑωυτοῦ γυναικός, ἐρασθεὶς δὲ ἐνόμιζέ οἱ εἶναι γυναῖκα πολλὸν πασέων καλλίστην. ὥστε δὲ ταῦτα νομίζων, ἦν γάρ οἱ τῶν αἰχμοφόρων Γύγης ὁ Δασκύλου ἀρεσκόμενος μάλιστα, τούτῳ τῷ Γύγῃ καὶ τὰ σπουδαιέστερα τῶν πρηγμάτων ὑπερετίθετο ὁ Κανδαύλης καὶ δὴ καὶ τὸ εἶδος τῆς γυναικὸς ὑπερεπαινέων.</p>

      <p>χρόνου δὲ οὐ πολλοῦ διελθόντος — χρῆν γὰρ Κανδαύλῃ γενέσθαι κακῶς — ἔλεγε πρὸς τὸν Γύγην τοιάδε. “Γύγη, οὐ γὰρ σε δοκέω πείθεσθαι μοι λέγοντι περὶ τοῦ εἴδεος τῆς γυναικός — ὦτα γὰρ τυγχάνει ἀνθρώποισι ἐόντα ἀπιστότερα ὀφθαλμῶν — ποίεε ὅκως ἐκείνην θεήσεαι γυμνήν.”</p>

      <p>ὃ δʼ ἀμβώσας εἶπε “δέσποτα, τίνα λέγεις λόγον οὐκ ὑγιέα, κελεύων με δέσποιναν τὴν ἐμὴν θεήσασθαι γυμνήν; ἅμα δὲ κιθῶνι ἐκδυομένῳ συνεκδύεται καὶ τὴν αἰδῶ γυνή.πάλαι δὲ τὰ καλὰ ἀνθρώποισι ἐξεύρηται, ἐκ τῶν μανθάνειν δεῖ· ἐν τοῖσι ἓν τόδε ἐστί, σκοπέειν τινὰ τὰ ἑωυτοῦ. ἐγὼ δὲ πείθομαι ἐκείνην εἶναι πασέων γυναικῶν καλλίστην, καὶ σέο δέομαι μὴ δέεσθαι ἀνόμων.” </p>
