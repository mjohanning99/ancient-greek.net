  <div class="column">
    <div class="article">
      <p>Croesus was of the line of Lydus, a child of Alyattes and the ruler of the people living on the Halys river. This river flows from the south (lit. mid day) between Syria and Paphlagonia and is sent out in the north into the Black Sea.</p>

      <p>This Croesus was the first of the barbarians whom we know of that conquered some Greeks and earned their respect and made friends of others. Those he conquered were the Ionians, Aioleans and Doreans in Asia; those he befriended were the Spartans. </p>

      <p>Before the reign of Croesus, all Greeks were free; for the Cimmerian campaign that attacked Ionia before Croesus’ reign did not subdue the cities, but rather raided and robbed them.</p>
