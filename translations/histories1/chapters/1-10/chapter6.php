      <p>Κροῖσος ἦν Λυδὸς μὲν γένος, παῖς δὲ Ἀλυάττεω, τύραννος δὲ ἐθνέων τῶν ἐντός Ἅλυος ποταμοῦ, ὃς ῥέων ἀπὸ μεσαμβρίης μεταξὺ Συρίων τε καὶ Παφλαγόνων ἐξιεῖ πρὸς βορέην ἄνεμον ἐς τὸν Εὔξεινον καλεόμενον πόντον.</p>

      <p>οὗτος ὁ Κροῖσος βαρβάρων πρῶτος τῶν ἡμεῖς ἴδμεν τοὺς μὲν κατεστρέψατο Ἑλλήνων ἐς φόρου ἀπαγωγήν, τοὺς δὲ φίλους προσεποιήσατο. κατεστρέψατο μὲν Ἴωνάς τε καὶ Αἰολέας καὶ Δωριέας τοὺς ἐν τῇ Ἀσίῃ, φίλους δὲ προσεποιήσατο Λακεδαιμονίους.</p>

      <p>πρὸ δὲ τῆς Κροίσου ἀρχῆς πάντες Ἕλληνες ἦσαν ἐλεύθεροι· τὸ γὰρ Κιμμερίων στράτευμα τὸ ἐπὶ τὴν Ἰωνίην ἀπικόμενον Κροίσου ἐὸν πρεσβύτερον οὐ καταστροφὴ ἐγένετο τῶν πολίων ἀλλʼ ἐξ ἐπιδρομῆς ἁρπαγή.</p>
  	</div>
