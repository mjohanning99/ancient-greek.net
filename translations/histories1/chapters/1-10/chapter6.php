      <p>Kroisos ēn Lydos men genos, pais de Alyatteō, tyrannos de ethneōn tōn entos Halyos potamou, hos rheōn apo mesambriēs metaxy Syriōn te kai Paphlagonōn exiei pros boreēn anemon es ton Euxeinon kaleomenon ponton.</p>

      <p>houtos ho Kroisos barbarōn prōtos tōn hēmeis idmen tous men katestrepsato Hellēnōn es phorou apagōgēn, tous de philous prosepoiēsato. katestrepsato men Iōnas te kai Aioleas kai Dōrieas tous en tē Asiē, philous de prosepoiēsato Lakedaimonious.</p>

      <p>pro de tēs Kroisou archēs pantes Hellēnes ēsan eleutheroi; to gar Kimmeriōn strateuma to epi tēn Iōniēn apikomenon Kroisou eon presbyteron ou katastrophē egeneto tōn poliōn allʼ ex epidromēs harpagē.</p>
    </div>
