      <p>ἐπεὰν δέ ἀπὸ τοῦ θρόνου στείχῃ ἐπὶ τὴν εὐνήν κατὰ νώτου τε αὐτῆς γένῃ, σοὶ μελέτω τὸ ἐνθεῦτεν ὅκως μὴ σε ὄψεται ἰόντα διὰ θυρέων.”</p>
