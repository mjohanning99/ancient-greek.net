      <p>He (Gyges) fought his master’s decision vehemently, fearing something bad might happen to him from his actions. Candaules, however, responds by saying, <q>Get a hold of yourself, Gyges! Do not be afraid of me — for I am only saying this to test you — nor of my wife nor fear that something bad will happpen to you because of her. For am I planning something so that she will never know she was seen by you.</p></q>

      <p><q>I will have you stand by the open door of the room in which we sleep. After my coming inside, my wife will already be in bed; she sleeps near the chair standing by the door. Onto this chair she places her garments, one by one — this is your opportunity to watch, quietly.</p></q>

      <p><q>When she gets up from the chair and turns her back to you to go to bed, I want you to leave through the door, but so that she does not see you.</p></q>
