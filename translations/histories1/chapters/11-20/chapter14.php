  <div class="column">
    <div class="article">
      <p>And in this way, the Mermnadae received the rule by taking it from the Heraclids; and during Gyges’ reign, he sent a not unsignificant amount of offerings consisting of silver (coins) to Delphi — the majority of which remain there. Aside from the silver, some other offerings of his that are worth mentioning are an immense amount of gold and six golden wine jugs, dedicated by him to Delphi.

      <p>These stood in the Treasury of Delphi, having a combined weight of thirty talents (roughly 790 kg / 1740 lbs). To tell you the truth, the Treasury does not belong to the people of Corinth themselves, but rather to Cypselus of Eetion (Corinthianruler of the 7th century BC). This Gyges was the first of the barbarians of whom we know, who placed offerings at Delphi; at least after Midas, the son of Gordia and king of Phrygia.</p>

      <p>This Midas placed the royal throne on which he sat and passed judgement, an item well worth seeing. The throne stands in the same place where Gyges’ wine jugs stand. The silver and gold, which Gyges dedicated to Delphi, is called <q>Gygean</q> by the Delphians, after the person who dedicated it.</p>
