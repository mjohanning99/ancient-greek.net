      <p>Tēn men dē tyrannida houtō eschon hoi Mermnadai tous Hērakleidas apelomenoi, Gygēs de tyranneusas apepempse anathēmata es Delphous ouk oliga, allʼ hosa men argyrou anathēmata, esti hoi pleista en Delphoisi, parex de tou argyrou chryson apleton anethēke allon te kai tou malista mnēmēn axion echein esti, krētēres hoi arithmon hex chryseoi anakeatai.</p>

      <p>hestasi de houtoi en tō Korinthiōn thēsaurō, stathmon echontes triēkonta talanta; alēthei de logō chreōmenō ou Korinthiōn tou dēmosiou esti ho thēsauros, alla Kypselou tou Ēetiōnos. houtos de ho Gygēs prōtos barbarōn tōn hēmeis idmen es Delphous anethēke anathēmata meta Midēn ton Gordieō Phrygiēs basilea.</p>

      <p>anethēke gar dē kai Midēs ton basilēion thronon es ton prokatizōn edikaze, eonta axiotheēton; keitai de ho thronos houtos entha per hoi tou Gygeō krētēres. ho de chrysos houtos kai ho argyros ton ho Gygēs anethēke, hypo Delphōn kaleetai Gygadas epi tou anathentos epōnymiēn.</p>
