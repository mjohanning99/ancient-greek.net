    <h2 style="text-align: center;">Introduction</h2>
    <p class="blocktext">Gyges has now finally become the king of the Lydians, though his popularity was less than stellar. Therefore, he, and those who opposed him, went to the Oracle at Delphi (also known as Pythia) to receive confirmation of Gyges’ reign. The Oracle gives both the confirmation and a warning.</p>
