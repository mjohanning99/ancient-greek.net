    <div class="article">
      <p>Ἒσχε δὲ τὴν βασιληίην καὶ ἐκρατύνθη ἐκ τοῦ ἐν Δελφοῖσι χρηστηρίου. ὡς γὰρ δὴ οἱ Λυδοὶ δεινόν ἐποιεῦντο τὸ Κανδαύλεω πάθος καὶ ἐν ὅπλοισι ἦσαν, συνέβησαν ἐς τὠυτὸ οἳ τε τοῦ Γύγεω στασιῶται καί οἱ λοιποὶ Λυδοί, ἤν μὲν τὸ χρηστήριον ἀνέλῃ μιν βασιλέα εἶναι Λυδῶν, τόν δὲ βασιλεύειν, ἤν δὲ μή, ἀποδοῦναι ὀπίσω ἐς Ἡρακλείδας τὴν ἀρχήν.</p>

      <p>ἀνεῖλέ τε δὴ τὸ χρηστήριον καὶ ἐβασίλευσε οὕτω Γύγης. τοσόνδε μέντοι εἶπε ἡ Πυθίη, ὡς Ἡρακλείδῃσι τίσις ἥξει ἐς τὸν πέμπτον ἀπόγονον Γύγεω. τούτου τοῦ ἔπεος Λυδοί τε καί οἱ βασιλέες αὐτῶν λόγον οὐδένα ἐποιεῦντο, πρὶν δὴ ἐπετελέσθη.</p>
    </div>
  </div>

  <div class="column">
    <div class="article">
      <p>Esche de tēn basilēiēn kai ekratynthē ek tou en Delphoisi chrēstēriou. hōs gar dē hoi Lydoi deinon epoieunto to Kandauleō pathos kai en hoploisi ēsan, synebēsan es tōuto hoi te tou Gygeō stasiōtai kai hoi loipoi Lydoi, ēn men to chrēstērion anelē min basilea einai Lydōn, ton de basileuein, ēn de mē, apodounai opisō es Hērakleidas tēn archēn.</p>

      <p>aneile te dē to chrēstērion kai ebasileuse houtō Gygēs. tosonde mentoi eipe hē Pythiē, hōs Hērakleidēsi tisis hēxei es ton pempton apogonon Gygeō. toutou tou epeos Lydoi te kai hoi basilees autōn logon oudena epoieunto, prin dē epetelesthē.</p>
    </div>
  </div>

  <div class="column">
    <div class="article">
      <p>He took over the reign, which was strenghthened by the Oracle at Delphi; for the Lydians considered the suffering of Candaules a terrible thing and they were, therefore, armed; so both Gyges’ supporters and the rest of the Lydians agreed that if the Oracle should consider him to be the king, he will be; should the Oracle decide against his being being king, the reign would fall back to the Heraclids.</p>

      <p>The Oracle picked Gyges and the reign, thus, belonged to him; but the Oracle said that some Heraclids will come for the fifth generation of Gyges’ descendants. The Lydians, and those who ruled them, did not care much about this; at least not, before it was not fulfilled</p>
