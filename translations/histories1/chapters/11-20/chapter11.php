      <p>Tote men dē houtō ouden dēlōsasa hēsychiēn eiche. hōs de hēmerē tachista egegonee, tōn oiketeōn tous malista hōra pistous eontas heōutē, hetoimous poiēsamenē ekalee ton Gygea. ho de ouden dokeōn autēn tōn prēchthentōn epistasthai ēlthe kaleomenos; eōthee gar kai prosthe, hokōs hē basileia kaleoi, phoitan.</p>

      <p>hōs de ho Gygēs apiketo, elege hē gynē tade. “nyn toi dyōn hodōn pareouseōn Gygē didōmi hairesin, hokoterēn bouleai trapesthai. ē gar Kandaulea apokteinas eme te kai tēn basilēiēn eche tēn Lydōn, ē auton se autika houtō apothnēskein dei, hōs an mē panta peithomenos Kandaulē tou loipou idēs ta mē se dei.</p>

      <p>allʼ ētoi keinon ge ton tauta bouleusanta dei apollysthai, ē se ton eme gymnēn theēsamenon kai poiēsanta ou nomizomena.” ho de Gygēs teōs men apethōmaze ta legomena, meta de hiketeue mē min anankaiē endeein diakrinai toiautēn hairesin.</p>

      <p>oukōn dē epeithe, allʼ hōra anankaiēn alētheōs prokeimenēn ē ton despotea apollynai ē auton hypʼ allōn apollysthai; haireetai autos perieinai. epeirōta dē legōn tade. “epei me anankazeis despotea ton emon kteinein ouk ethelonta, phere akousō teō kai tropō epicheirēsomen autō.”
      hē de hypolabousa ephē “ek tou autou men chōriou hē hormē estai hothen per kai ekeinos eme epedexato gymnēn, hypnōmenō de hē epicheirēsis estai.” </p>
