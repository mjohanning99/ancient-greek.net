      <p>Τότε μὲν δὴ οὕτω οὐδέν δηλώσασα ἡσυχίην εἶχε. ὡς δὲ ἡμέρη τάχιστα ἐγεγόνεε, τῶν οἰκετέων τοὺς μάλιστα ὥρα πιστοὺς ἐόντας ἑωυτῇ, ἑτοίμους ποιησαμένη ἐκάλεε τὸν Γύγεα. ὁ δὲ οὐδὲν δοκέων αὐτήν τῶν πρηχθέντων ἐπίστασθαι ἦλθε καλεόμενος· ἐώθεε γὰρ καὶ πρόσθε, ὅκως ἡ βασίλεια καλέοι, φοιτᾶν.</p>

      <p>ὡς δὲ ὁ Γύγης ἀπίκετο, ἔλεγε ἡ γυνὴ τάδε. “νῦν τοί δυῶν ὁδῶν παρεουσέων Γύγη δίδωμί αἵρεσιν, ὁκοτέρην βούλεαι τραπέσθαι. ἢ γὰρ Κανδαύλεα ἀποκτείνας ἐμέ τε καὶ τὴν βασιληίην ἔχε τὴν Λυδῶν, ἢ αὐτόν σε αὐτίκα οὕτω ἀποθνήσκειν δεῖ, ὡς ἂν μὴ πάντα πειθόμενος Κανδαύλῃ τοῦ λοιποῦ ἴδῃς τὰ μὴ σε δεῖ.</p>

      <p>ἀλλʼ ἤτοι κεῖνόν γε τὸν ταῦτα βουλεύσαντα δεῖ ἀπόλλυσθαι, ἢ σε τὸν ἐμὲ γυμνήν θεησάμενον καὶ ποιήσαντα οὐ νομιζόμενα.” ὁ δὲ Γύγης τέως μὲν ἀπεθώμαζε τὰ λεγόμενα, μετὰ δὲ ἱκέτευε μὴ μιν ἀναγκαίῃ ἐνδέειν διακρῖναι τοιαύτην αἵρεσιν.</p>

      <p>οὔκων δὴ ἔπειθε, ἀλλʼ ὥρα ἀναγκαίην ἀληθέως προκειμένην ἢ τὸν δεσπότεα ἀπολλύναι ἢ αὐτὸν ὑπʼ ἄλλων ἀπόλλυσθαι· αἱρέεται αὐτὸς περιεῖναι. ἐπειρώτα δὴ λέγων τάδε. “ἐπεί με ἀναγκάζεις δεσπότεα τὸν ἐμὸν κτείνειν οὐκ ἐθέλοντα, φέρε ἀκούσω τέῳ καὶ τρόπῳ ἐπιχειρήσομεν αὐτῷ.”
      ἣ δὲ ὑπολαβοῦσα ἔφη “ἐκ τοῦ αὐτοῦ μὲν χωρίου ἡ ὁρμή ἔσται ὅθεν περ καὶ ἐκεῖνος ἐμέ ἐπεδέξατο γυμνήν, ὑπνωμένῳ δὲ ἡ ἐπιχείρησις ἔσται.” </p>
