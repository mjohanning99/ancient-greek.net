      <p>She, therefore, said nothing of this and held her silence. As it swiftly turned day, she selected of the servants those she trusted the most and had them taken to her; Gyges was then called. As he thought that she was unaware of what had been done, he came as ordered; for he was accustomed to coming to the mistress whenever he was summoned by her.</p>

      <p>Once Gyges had arrived, she said the following: <q>I am now giving you the choice between two paths; tell me which one it will be. Either you kill Candaules and, in turn, get me and the Lydian kingdom; or you yourself will have to die immediately, so that you will not be exposed to further improper acts by obeying Candaules.</q></p>

      <p><q>But it is inevitable that either the contriver of this plot must be killed, or you, who has done an improper act by seeing me naked.</q> So far, Gyges had been marvelling at what she was saying, but once she was finished, he begged her not to make him decide between such things.</p>

      <p>He was unable to deter her and saw that, truly, he must decide between the killing of his master or being, himself, killed by others; he decided to keep his life. He, then, asked her the following: <q>As you are forcing me — against my will — to kill my master, just go ahead and tell me in what manner you want us to attack him.</q>
      She replied, <q>The attack will take place in the same room in which he put the naked me up for display — the attack will occur whilst he sleeps.</q>
