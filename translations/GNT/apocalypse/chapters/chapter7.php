    <p>After this I saw four angels standing upon the four corners of the Earth (and I saw them) taking the seven winds of the Earth, so that (the) wind shall not blow on the (face of the) Earth nor on (over?) the sea, nor on all (any) trees.</p>

    <p>And I saw another angel coming down from the East (lit. Eastern sun), having (in his hands) the seal of the living God; and he was shouting in a great voice to the four angels — (those) to whom (the authority) to hurt the Earth and the sea was given — saying, “Do not harm the Earth, nor the sea, nor the trees until we seal the servants of our God upon their foreheads. And I heard the number of the sealed (and that number was) one-hundred forty-four thousand (sealed) from every tribe of the children of Israel:</p>

    <p>From the tribe of Juda (there were) twelve-thousand sealed; from the tribe of Ruben twelve-thousand; from the tribe of Gad twelve-thousand; from the tribe of Aser twelve-thousand; from the tribe of Naphthalim twelve-thousand; from the tribe of Manasses twelve-thousand; from the tribe of Simeon twelve-thousand; from the tribe of Levi twelve-thousand; from the tribe of Issachar twelve-thousand; from the tribe of Zabulon twelve-thousand; from the tribe of Joseph twelve-thousand; (and) from the tribe of Benjamin (there were) twelve-thousand sealed. </p>

    <p>After this I saw — and look! — a great crowd (the number of people in) which nobody was able to count, from every people and tribe and nation and tongue (language); (and they were) standing before the throne and before the Lamb (and they were all) clad in long, white robes and (they all had) palms (date palms, type of tree) in their hands. And they shout(ed) in a great voice saying, “(The) salvation (be) to our God and to the One sitting upon the throne and to the Lamb.”</p>

    <p>And all the angels and elders and the four creatures were standing around the throne and they fell to their face before the throne and worshipped God saying, “Amen. The blessing and glory and wisdom and gratitude and power and might and strength (be) to our God into the eternity of eternities. Amen.” </p>

    <p>And of the elders (there was one) answering and telling me, “Those (over there), the ones who are clad in the white robes — who are they and where did they come from?” And I told him, “My lord, you know.” And he said to me, “They are those who come from the great oppression and they washed their robes and whitened them in (with) the blood of the Lamb. Because of (through) this, they are before the throne of God and they serve Him in His temple day and night; and He who sits upon the throne will erect his tent (dwell, live) on them. And they are neither hungry nor thirsty anymore; nor will the Sun fall upon them (shine on them?) nor all (any) heat. For the Lamb which is in the middle of the throne will feed them and show them the way to the living fountain of waters; and God will wipe away every tear from their eyes. </p>
