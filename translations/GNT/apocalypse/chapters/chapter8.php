    <p>And the second angel sounded (his trumpet). And (something that was) like a great, fiery mountain was hurled into the sea. And a third of the sea became blood, and a third of the creatures of the sea — namely those who live in the sea and have a soul (who are alive) — died; and a third of the ships were destroyed. </p>

    <p>And the third angel sounded (his trumpet). And (there) fell from (the) Heaven a great start, burning like a torch; and it fell into (upon) a third of the rivers and into (upon) the springs of waters. And the name of the star is Wormwood (Apsinthos). And a third of the waters became wormwood and many (of the) humans died because of the waters, because they were made bitter. </p>

    <p>And the fourth angel sounded (his trumpet). And a third of the Sun was struck, and a third of the Moon and a third of the stars, so that a third of them was darkened (shadowed) and (so that) the day — as well as night — does not shine (upon) a third of them. And I saw and I heard an eagle flying in mid-air saying, “Woe, woe, woe to those who live upon the Earth from the remaining trumpet voices of the three angels that are about to sound (their trumpets).</p>
