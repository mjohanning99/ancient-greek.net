  <p>The epistles — a Greek word (<q>ἐπιστολή</q>) which literally translates to <q>letter</q> — make up the majority of the new Testament, with 21 canonical epistles — most of which (fourteen to be precise) were written by Paul — being included in the canon. Of these twenty-one pieces of literature belonging to the epistolic genre, seven fall under the so-called <q>catholic</q> (or general) category. Amongst the authors of the latter category are John — who is generally not believed to be the John of the Apocalypse, nor the John of the Gospel — and Peter, whom some believe to be Simon Peter, one of the original apostles of Jesus. </p> 
