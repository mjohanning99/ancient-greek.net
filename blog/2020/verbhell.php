    <h2>Principal parts and augment</h2>

    <p>Firstly, as I stated in my previous post, I have slowly started going through the second Unit of Hansen and Quinn&#39;s book and have, thus far, done nearly all the drills and will soon continue with the exercises; my goal of finishing this by the end of next week will, therefore, be quite easily attained. However, I was rather unprepared for the number of things one is required to learn in order to be able to form all possible forms of a Greek verb since, before you are even able to <em>begin</em> your attempt and conjugating a specific verb, you are required to learn the verb; but, unlike English and most other languages I have learnt so far, you are required to learn <em>six</em> principal parts for each verb. Frequently, they appear to be formed using some type of pattern, but often, each principal part cannot be derived from any other principal part. This is similar to how you must learn several parts of irregular English verbs — go, went, gone, going — in order to form all possible tenses and moods and such, but taken to the absolute extreme.
    Indeed, it appears as if Ancient Greek has retained most — if not all — of the complexity of the Proto-Indo-European verb. Thus, if you wish to learn the verb for “to teach, to educate”, you will have to learn the following six principal parts of that verb: <em>παιδεύω, παιδεύσω, ἐπαίδευσα, πεπαίδευκα, πεπαίδευμαι, ἐπαιδεύθην</em>. </p>

    <p>There also exists the so-called <em>past indicative augment</em> <em>“ἐ-”</em> which can be seen being attached to two of the above-described principal parts. It can also be prefixed to the first principal part&#39;s root, <em>παιδευ-</em>, to form <em>ἐπαιδευ-</em>. This, when the correct endings have been added onto it, is used to express the imperfect indicative. But speaking of endings …</p>
  </div>
