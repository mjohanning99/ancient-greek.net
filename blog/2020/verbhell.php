    <h2>Endings</h2>

    <p>There are quite a few of those in Ancient Greek. Indeed, unlike most other modern European languages where you generally only have a couple of endings — often depending on the verb&#39;s ending —, Ancient Greek has quite a large number of them. I have thus far only learnt a minuscule amount of them as I am only at Unit 2, but they will be introducing more of them over the upcoming units. </p>

    <p>For example, let us take the above-mentioned example of <em>παιδευ-</em> vs. <em>ἐπαιδευ-</em>; even though the latter was formed from the former by simply having a prefix attached to it, the endings are different. Not <em>drastically</em> so, but enough to get, perhaps, slightly confused. Thus, <em>παιδεύω</em> means “I teach”, whereas <em>ἐπαιδεύον</em> translates as “I was teaching”; the ending was <em>-ω</em> in the present indicative active but <em>-ον</em> in the imperfect indicative active.</p>

    <p>I have found that identifying the various ending and principal part combinations is <em>somewhat</em> easy to accomplish, but actually forming any of these verb forms is rather difficult; and I am certain that this will become even more difficult — this includes the identification of the various forms as well as the formation of them — as more verb forms are introduced, especially the middle and passive voices and the subjunctive and optative moods. </p>
