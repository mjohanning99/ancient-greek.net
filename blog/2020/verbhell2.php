<div class="article">
  <h2>Their Usage</h2>
  <p>But where are these moods <em>actually</em> used? Well, that appears to be a rather difficult question to answer, but they frequently appear in so-called <em>purpose clauses</em> which, in English, are generally introduced by phrases such as “in order that” or “so that”. Greek here uses three different kinds of words, namely either ἵνα, ὅπως or ὡς. Then, depending on whether the main verb is in the primary (Present, Future or Perfect) or secondary (Imperfect, Aorist, Pluperfect) sequence, the verb within the purpose clause will be in the subjunctive or optative; primary sequence verbs call of the subjunctive whereas secondary sequence ones require the optative. </p>

  <p>Secondly, it is important to note that the subjunctive present and aorist do <em>not</em> actually refer to time, but aspect only; thus, the present subjunctive — as well as the optative — refers to repeated aspect, whereas the aorist refers to completed aspect.</p>

